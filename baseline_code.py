
import re

def is_valid_ipv6(ip):
    pattern = re.compile(
        r'^(?:'
        r'(?:[0-9a-fA-F]{1,4}:){7,7}[0-9a-fA-F]{1,4}|'  # 1:2:3:4:5:6:7:8
        r'(?:[0-9a-fA-F]{1,4}:){1,7}:|'  # 1::                              1:2:3::7:8
        r'(?:[0-9a-fA-F]{1,4}:){1,6}:[0-9a-fA-F]{1,4}|'  # 1::8             1:2:3:4:5::8
        r'(?:[0-9a-fA-F]{1,4}:){1,5}(?::[0-9a-fA-F]{1,4}){1,2}|'  # 1::7:8     1:2:3:4:5:6::8
        r'(?:[0-9a-fA-F]{1,4}:){1,4}(?::[0-9a-fA-F]{1,4}){1,3}|'  # 1::6:7:8   1:2:3:4:5::8
        r'(?:[0-9a-fA-F]{1,4}:){1,3}(?::[0-9a-fA-F]{1,4}){1,4}|'  # 1::5:6:7:8 1:2:3:4::8
        r'(?:[0-9a-fA-F]{1,4}:){1,2}(?::[0-9a-fA-F]{1,4}){1,5}|'  # 1::4:5:6:7:8 1:2:3::8
        r'[0-9a-fA-F]{1,4}:(?:(?::[0-9a-fA-F]{1,4}){1,6})|'  # 1::3:4:5:6:7:8  1:2::8
        r':(?:(?::[0-9a-fA-F]{1,4}){1,7}|:)|'  # ::2:3:4:5:6:7:8  ::8
        r'fe80:(?::[0-9a-fA-F]{0,4}){0,4}%[0-9a-zA-Z]{1,}|' #fe80::7:8  fe80::7:8%eth0
        r'::(?:ffff(?::0{0,4}){0,1}:){0,1}(?P<address>[0-9.]{7,15})|'
        r'(?:[0-9a-fA-F]{1,4}:){1,4}(?P<address>:[0-9.]{7,15})'
        r')$'
    )
    
    if pattern.match(ip):
        return True
    else:
        return False

# Test the function
ip_address = "2001:0db8:85a3:0000:0000:8a2e:0370:7334"
if is_valid_ipv6(ip_address):
    print(f"{ip_address} is a valid IPv6 address.")
else:
    print(f"{ip_address} is not a valid IPv6 address.")
